<h1>Security Posture</h1>
<p>
  The Ars Rosaic maintains a posture of absolute vigilance, utilizing asymmetrical encryption to protect institutional integrity.
</p>
<p>
<strong>PGP Encryption & Identity</strong>
For all sensitive communications regarding the website, the organiztion mandates the use of pretty good privacy (pgp). Our public key is available for verification to ensure that all reports remain confidential and untampered with during transit. You may retrieve our key at <a href="/pgp_key">PGP Key</a>.
</p>

<p>
<strong>Access and Encryption</strong><br>
The Organization utilizes advanced encryption standards and rigorous access control measures.
</p>
<p>
<strong>Responsible Disclosure (rfc 9116)</strong><br>
We take responsible disclosure seriously. If you discover a vulnerability, please consult our <a href="/security.txt">security txt file</a> file for contact instructions. We require adherence to the following stance:
<ul>
  <li> Encrypt all vulnerability reports using our <a href="/pgp_key">PGP Key</a>.</li>
  <li> Do not publicly disclose findings until the Office has confirmed remediation.</li>
  <li> Do not exploit vulnerabilities beyond the minimum necessary for a proof of concept.</li>
</ul>
</p>
<p>
<strong>Logging and Forensic Audit</strong><br>
Unauthorized attempts to bypass security or access administrative paths are logged and reported. Metadata regarding these events is archived permanently.
</p>
